package com.str;

public class StringLength {

	public static void main(String[] args) {
		String s="VasanthKumar";
		System.out.println("Number of characters in a string = "+s.length());
		//substring
		System.out.println("part of the string "+s.substring(3));
		System.out.println("part of the string "+s.substring(2,12));
	}

}
